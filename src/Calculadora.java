
public class Calculadora
{
	public int adicao(int v1, int v2)
	{
		return v1 + v2;
	}
	
	public int subtracao(int v1, int v2)
	{
		return v1 - v2;
	}
	
	public float multiplica(int v1, int v2)
	{
		return v1 * v2;
	}
	
	public float dividir(int v1, int v2)
	{
		return v1 / v2;
	}

}
